import { access, readFile, writeFile } from "node:fs/promises"
import { constants } from "node:fs"
import { relative, resolve } from "node:path"
import { pathToFileURL } from "node:url"

function readArg(name) {
  const index = process.argv.indexOf(name)
  if (index === -1) {
    return undefined
  }
  const value = process.argv[index + 1]
  if (value === undefined || value.startsWith("--")) {
    throw new Error(`${name} requires a value`)
  }
  return value
}

export function inferDistTag(version) {
  if (version.includes("-alpha.")) {
    return "alpha"
  }
  if (version.includes("-beta.")) {
    return "staging"
  }
  if (version.includes("-rc.")) {
    return "next"
  }
  if (version.includes("-")) {
    return "next"
  }
  return "latest"
}

function renderReleaseBody({ tagName, version, distTag, releaseNotes, releaseNotesPath }) {
  return `<!-- Generated by scripts/generate-github-release-notes.mjs. Edit ${releaseNotesPath} for release content. -->

${releaseNotes.trim()}

---

## Automation Metadata

- Source release notes: \`${releaseNotesPath}\`
- Package version: \`${version}\`
- Tag: \`${tagName}\`
- Intended npm dist-tag: \`${distTag}\`
- Acceptance results index: \`docs/current/acceptance-results/README.md\`

## Claim Boundaries

This GitHub release body is generated from the repository release notes. It does
not add token-saving, provider-token-saving, product-efficacy,
navigation-benefit, app-quality, full-TDD/test-sufficiency, CodeGraph/LSP
default/effectiveness, broad reliability, closure guarantee, Codex support,
code-nav replacement, or automatic downgrade/removal claims.

It also does not select a current workflow lifecycle state or prove #111
producer success, signature verification, registry publication, tag movement,
GitHub release creation, or release completion.
`
}

async function fileExists(path) {
  try {
    await access(path, constants.R_OK)
    return true
  } catch {
    return false
  }
}

async function main() {
  const projectArg = readArg("--project")
  const projectDir = projectArg === undefined ? process.cwd() : resolve(projectArg)
  const outPath = readArg("--out")
  const checkOnly = process.argv.includes("--check")

  const packagePath = resolve(projectDir, "package.json")
  const packageJson = JSON.parse(await readFile(packagePath, "utf8"))
  const tagName = readArg("--tag") ?? process.env.TAG_NAME ?? `v${packageJson.version}`

  if (!tagName.startsWith("v")) {
    throw new Error(`Release tag must start with v: ${tagName}`)
  }

  const version = tagName.slice(1)
  if (version !== packageJson.version) {
    throw new Error(
      `Tag ${tagName} does not match package.json version ${packageJson.version}`,
    )
  }

  const releaseNotesPath = `docs/current/release/v${version}-release-notes.md`
  const absoluteReleaseNotesPath = resolve(projectDir, releaseNotesPath)
  if (!(await fileExists(absoluteReleaseNotesPath))) {
    throw new Error(`${releaseNotesPath} does not exist`)
  }

  const releaseNotes = await readFile(absoluteReleaseNotesPath, "utf8")
  const body = renderReleaseBody({
    tagName,
    version,
    distTag: inferDistTag(version),
    releaseNotes,
    releaseNotesPath,
  })

  if (!body.includes(`# v${version} Release Notes`)) {
    throw new Error(`${releaseNotesPath} must include a v${version} release heading`)
  }

  if (checkOnly) {
    console.log(
      `GitHub release notes check: PASS (${tagName}, ${releaseNotesPath})`,
    )
    return
  }

  if (outPath === undefined) {
    process.stdout.write(body)
    return
  }

  const absoluteOutPath = resolve(projectDir, outPath)
  await writeFile(absoluteOutPath, body)
  console.log(`GitHub release notes written: ${relative(projectDir, absoluteOutPath)}`)
}

if (process.argv[1] !== undefined && import.meta.url === pathToFileURL(process.argv[1]).href) {
  main().catch((error) => {
    console.error(error instanceof Error ? error.message : String(error))
    process.exitCode = 1
  })
}
